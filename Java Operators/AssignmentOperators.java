public class AssignmentOperators {
    public static void main(String[] args) {

        // create variables
        int a = 5;
        // assign value using =
        System.out.println("var using =: " + a);
          
        // assign value using +=
        int b = 5;
        b += 3;
        System.out.println("var using +=: " + b);
        
        // assign value using -=
        int c = 5;
        c -= 3;
        System.out.println("var using -=: " + c);
        
        // assign value using *=
        int d = 5;
        d *= 3;
        System.out.println("var using *=: " + d);
        
        // assign value using /=
        int e = 5;
        e /= 3;
        System.out.println("var using /=: " + e);
        
        // assign value using %=
        int f = 5;
        f %= 3;
        System.out.println("var using %=: " + f);

        // assign value using &=
        int g = 5;
        g &= 3;
        System.out.println("var using &=: " + g);

        // assign value using |=
        int h = 5;
        h |= 3;
        System.out.println("var using |=: " + h);

        // assign value using ^=
        int i = 5;
        i ^= 3;
        System.out.println("var using ^=: " + i);

        // assign value using >>=
        int j = 5;
        j >>= 3;
        System.out.println("var using >>=: " + j);

        // assign value using <<=
        int k = 5;
        k <<= 3;
        System.out.println("var using <<=: " + k);
        
        // assign value using >>>=
        int l = 5;
        l >>>= 3;
        System.out.println("var using >>>=: " + l);

    }   
}
